import base64


def score_english_text(text):
    letter_frequency = {
        'a': .08167, 'b': .01492, 'c': .02782, 'd': .04253,
        'e': .12702, 'f': .02228, 'g': .02015, 'h': .06094,
        'i': .06094, 'j': .00153, 'k': .00772, 'l': .04025,
        'm': .02406, 'n': .06749, 'o': .07507, 'p': .01929,
        'q': .00095, 'r': .05987, 's': .06327, 't': .09056,
        'u': .02758, 'v': .00978, 'w': .02360, 'x': .00150,
        'y': .01974, 'z': .00074, ' ': .15000
    }
    return sum([letter_frequency.get(chr(i), 0) for i in text.lower()])


def single_char_xor(input_bytes, char_value):
    return bytes([i ^ char_value for i in input_bytes])


def repeating_key_xor(input_bytes, key_bytes):
    return bytes([input_bytes[i] ^ key_bytes[i % len(key_bytes)] for i in range(len(input_bytes))])


def hamming_dist(byte_str1, byte_str2):
    return sum(bin(i ^ j).count('1') for i, j in zip(byte_str1, byte_str2))


def decrypt_single_byte_xor(input_bytes):
    candidates = [
        {
            'char_value': char_value,
            'decrypted': single_char_xor(input_bytes, char_value),
            'score': score_english_text(single_char_xor(input_bytes, char_value))
        }
        for char_value in range(256)
    ]
    best_candidate = max(candidates, key=lambda x: x['score'])
    return best_candidate


def determine_keysize(ciphertext):
    distances = [
        {
            'keysize': keysize,
            'avg_distance': sum(
                hamming_dist(ciphertext[i:i + keysize], ciphertext[i + keysize:i + 2 * keysize]) / keysize
                for i in range(0, len(ciphertext) - 2 * keysize, keysize)
            ) / (len(ciphertext) // keysize - 1)
        }
        for keysize in range(2, 41)
    ]
    return min(distances, key=lambda x: x['avg_distance'])


def break_repeating_key_xor(ciphertext):
    keysize_info = determine_keysize(ciphertext)
    keysize = keysize_info['keysize']

    key = b''
    blocks = [ciphertext[i:i + keysize] for i in range(0, len(ciphertext), keysize)]

    for i in range(keysize):
        transposed_block = bytes(block[i] for block in blocks if len(block) > i)
        candidate = decrypt_single_byte_xor(transposed_block)
        key += bytes([candidate['char_value']])

    decrypted_message = repeating_key_xor(ciphertext, key)
    return decrypted_message, key


